package brizlapsarrayhandling;
//150 and 250
public class Prectice {
      public static void main(String args[]) {
    	  int a=150;
    	  int b=250;
    	  System.out.println("print the value before swapping:");
    	  System.out.println("a = "+ a +" "+"b =  "+ b);
    	  int temp;
    	  temp=a;
    	  a=b;
    	  b=temp;
    	  System.out.println("After Swapping ");
    	  System.out.println("a = " + a + " "+"b = " +b);
      }
}
