package brizlapsoperator;

public class TwoNumbers {
      public static void main(String args[]) {
    	  int a=2;
    	  int b=4;
    	  int c=5;
    	  
    	  int res1=a+b*c;
    	  System.out.println(res1);
    	  
    	  int res2=c+a/b;
    	  System.out.println(res2);
    	  
    	  int res3=a%b+c;
    	  System.out.println(res3);
    	  
    	  
    	  int res4=a*b+c;
    	  System.out.println(res4);
      }
}
